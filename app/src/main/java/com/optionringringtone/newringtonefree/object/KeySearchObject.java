package com.optionringringtone.newringtonefree.object;

public class KeySearchObject {
    String id, keySearch;

    public KeySearchObject() {
    }

    public KeySearchObject(String id, String keySearch) {
        this.id = id;
        this.keySearch = keySearch;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKeySearch() {
        return keySearch;
    }

    public void setKeySearch(String keySearch) {
        this.keySearch = keySearch;
    }
}
